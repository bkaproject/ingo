package com.cavalerie.vlc123.gogogo.Adapters.History;

public class Grouped_date {

    int id;
    String date;

    public Grouped_date(int i, String n)
    {
        id = i;
        date = n;
    }

    public int getId() {
        return id;
    }

    public String getDate() {
        return date;
    }
}
